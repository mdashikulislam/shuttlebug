<div id="help_ts" class="help dark-blue">
    <h5 class="text-center">Tripsheet</h5>
    <p>The trip sheet is arranged by pickup times. Tap on the pickup time header to open the details.</p>
    <p>The details contain the passengers to be picked up and those to be dropped off.</p>
    <p>Tap on the pickup passenger to open their profile which has a photo, contact details and a <b>No Show</b> button to confirm if the passenger is not available for pickup.</p>
    <p>Tap on the drop-off passenger to access the <b>signature</b> page and a button to <b>send an sms</b> when the passenger is dropped off.</p>
    <p>Tap on the <b>Confirm Departure</b> button when leaving the pickup venue with all the passengers.</p>
    <p>Tap on the <b>View Route Map</b> button to see the map route locations for the drop offs.</p>
</div>

<div id="help_profile" class="help dark-blue d-none">
    <h5 class="text-center">Profile</h5>
    <p>This page has the profile of the passenger.</p>
    <p>If there is no photo, take a photo with your iPad and email it to the webmaster together with the passenger's name.</p>
    <p>Contact details are provided for the passenger's guardians in case of emergency.</p>
    <p>If the passenger is not at the pickup venue, tap the <b>No Show</b> button to notify the office.</p>
</div>

<div id="help_sms" class="help dark-blue d-none">
    <h5 class="text-center">Signature</h5>
    <p>Use this page when dropping off a passenger.</p>
    <p>The person receiving the passenger must sign in the signature pad. You can clear the signature pad by tapping the <b>Clear Signature</b> button in the event of a mistake.</p>
    <p>Once the Receiver has signed, tap the <b>Save Signature</b> button. This will send the signature to the office <b>as well as send an sms to the parent</b>.</p>
    <p>If you are not able to get a signature tap the <b>Send SMS</b> button to just send an sms confirming that the passenger has been dropped-off.</p>
    <p>You will receive a confirmation from the office when sending a signature or sms.</p>
</div>

<div id="help_map" class="help dark-blue d-none">
    <h5 class="text-center">Map</h5>
    <p>This page shows the route to be followed to drop off the passengers.</p>
    <p>Tap on any marker to see details for that point.</p>
    <p>The <b>green marker</b> is the pickup point and lists all the passengers being picked up.</p>
    <p>Each <b>blue marker</b> is a drop-off point and gives the passenger name and address.</p>
    <p>The <b>red marker</b> is the last drop-off point whereafter you should proceed to the next pickup on the tripsheet.</p>
    <p>The map can be panned by dragging on it and you can zoom in and out by pinching/expanding using two fingers.</p>
</div>